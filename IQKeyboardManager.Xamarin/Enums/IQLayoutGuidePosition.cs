namespace IQKeyboardManager.Xamarin
{
	public enum IQLayoutGuidePosition
	{
		/// <summary>
		/// If there are no IQLayoutGuideConstraint associated with view controller
		/// </summary>
		None,

		/// <summary>
		/// If provided IQLayoutGuideConstraint is associated with view controller's topLayoutGuide
		/// </summary>
		Top,

		/// <summary>
		/// If provided IQLayoutGuideConstraint is associated with view controller's bottomLayoutGuide
		/// </summary>
		Bottom
	}
}
